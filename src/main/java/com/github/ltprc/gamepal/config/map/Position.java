package com.github.ltprc.gamepal.config.map;

public class Position {

    private int x;
    private int y;
    private int sceneNo;
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
    public int getSceneNo() {
        return sceneNo;
    }
    public void setSceneNo(int sceneNo) {
        this.sceneNo = sceneNo;
    }
}
